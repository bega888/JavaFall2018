package j008_Void;

public class Person {

	String name;
	int age;
	char gender;
	String job;
	int salary;
	
	public void say(String speech) {
		System.out.println(speech);
	}
	public void  swim() {
		System.out.println("swimming");
		}
	public void  run() {
		System.out.println(name+" is running ");
		}
	public void  bike(int miles) {
		System.out.println(name+" is biking "+miles+" miles");
		}
	
}
