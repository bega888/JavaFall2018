package j012_operatorsIfElse;

public class Shorthands {
public static void main(String[] args) {
	int ppl=5;
	ppl=ppl+2;   //addition and assigning a new value
	System.out.println(ppl);
	ppl+=3;      //short way of addition and assigning a new value
	System.out.println(ppl);
	
	ppl*=2;
	System.out.println(ppl);
	ppl*=ppl;
	System.out.println(ppl);
	
	ppl/=2;
	System.out.println(ppl);
	ppl%=2;
	System.out.println(ppl);
	
}
}
