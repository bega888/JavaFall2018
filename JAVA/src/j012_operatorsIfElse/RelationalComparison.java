package j012_operatorsIfElse;

public class RelationalComparison {
	public static void main(String[] args) {
		int ppl1=5, ppl2=6;
		System.out.println(ppl1 > ppl2);
		
		boolean comp=ppl1>ppl2;
		System.out.println(comp);
		
		int apples=11;
		int oganges=13;
		System.out.println(apples == oganges);
		
		char l1='a';
		char l2='b';
		comp = l1<l2;
		System.out.println(comp);
		l1++;
		System.out.println(l1);
		l1++;
		System.out.println(l1);
		
		
		
	}

}
