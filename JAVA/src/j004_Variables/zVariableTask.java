package j004_Variables;

public class zVariableTask {
	public static void main(String [] args) {
		double cucumber=2.5, tomatoes=3, onion=1.5, pepper=2, carrot=4;
		System.out.println("1 Cucumber is "+cucumber+" dollars");
		System.out.println("1 Tomatoes is "+tomatoes+" dollars");
		System.out.println("1 Onion is "+onion+" dollars");
		System.out.println("1 Pepper is "+pepper+" dollars");
		System.out.println("1 Carrot is "+carrot+" dollars");
	}

}
