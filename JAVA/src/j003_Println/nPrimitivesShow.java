package j003_Println;

public class nPrimitivesShow {
	public static void main(String[] args) {
		
		byte apples=2, oranges=7;
		System.out.println("I have "+apples+" apples and "+oranges+" oranges");
		
		short girls=34, boys=98;
		System.out.println("In our campus we have "+girls+" girls and "+boys+" boys");
		
		int lyft=234, uber=467;
		System.out.println("Last week I have earned $"+lyft+" from Lyft and $"+uber+" from Uber.");
		
		long year1=2019L , year2=2020L;
		System.out.println("In "+year1+" I will start to make +100K and in "+year2+" I will buy my own house.");
		
		float marlbro=11.40f, parliament=12.38f;
		System.out.println("In 7eleven Marlbro pack is $"+marlbro+", and Parliament pack is $"+parliament);
		
		double propertyTaxIL=3.65, propertyTaxTX=1.85;
		System.out.println("Property tax in Illinois is %"+propertyTaxIL+", and in Texas it is as low as %"+propertyTaxTX); 
		
		char dollar='$', lira='L';
		System.out.println("Dollar sign is: "+dollar+", and Turkish Lira sign is: "+lira);
		
		boolean dubaiHot=true, alaskaHot=false;
		System.out.println("Is Dubai hot? "+dubaiHot+". Is Alaska hot? "+alaskaHot );
		
		
		
	}

}
