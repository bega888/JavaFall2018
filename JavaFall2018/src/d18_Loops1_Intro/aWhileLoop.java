package d18_Loops1_Intro;

public class aWhileLoop {
	public static void main(String[] args) {
		int b=0;
		while(b<10){
			System.out.println("Hello Wordl!");
			b++;
		}
		
		int counter=1;
		while(counter<15) {
			System.out.println(counter);
			counter++;
		}
		
		
		
	}

}
