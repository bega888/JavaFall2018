package d08_conditionals2;

public class VotingEliigibilityCheck {
	public static void main(String[] args) {
		/*create age variable int type, and assign a value
		 * check if value of age is 18 or more,
		 * true->you are eligible to vote
		 * false->you are not yet eligible to vote
		 */
		int age=17;
		if(age>=18) {
			System.out.println("You are eligible to vote");
		}else {
			System.out.println("You are not yet eligible to vote");
		}
		
		
		
	}

}
