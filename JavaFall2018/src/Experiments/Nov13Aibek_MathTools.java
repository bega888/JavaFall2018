package Experiments;

public class Nov13Aibek_MathTools {

	public static double P=3.14;
	public Nov13Aibek_MathTools() {}
	
	public static int areaOfRectangle(int lenght, int width) {
		return lenght*width;
	}
	
	public static int perimeterOfRectangle(int lenght, int width) {
		return 2*(lenght+width);
	}
	
	public static double areaOfCircle(int radious) {
		return P*radious*radious;
	}
	
	
	
}
