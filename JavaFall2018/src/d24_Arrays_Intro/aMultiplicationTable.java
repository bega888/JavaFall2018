package d24_Arrays_Intro;

public class aMultiplicationTable {
	public static void main(String[] args) {
		int k;
		for(int i=1; i<=10; i++) {
			System.out.println(" ---------------");
			for(int j=1; j<=10; j++ ) {
				k=i*j;
			System.out.println("|    "+i+"x"+j+"="+k+"\t|");	
			}
			
		}
		
		
		
		
		
		
		
		
	}

}
